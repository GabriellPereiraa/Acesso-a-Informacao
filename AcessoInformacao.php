                    <div class="col">
                    </div>
                    <div class="col-11">
                        <p class="Texto">
                            Um outro exemplo de pedido de informação que se afasta das finalidades da legislação de acesso à informação diz respeito ao pedido de acesso desproporcional. Aqui, deve ser dito que a informação, para ser fornecida, também não deve comprometer, significativamente, a realização das atividades rotineiras da instituição requerida, acarretando prejuízo injustificado aos direitos de outros solicitantes.
