                        </p>

                        <p class="Texto">
                            No segundo caso, pode-se indicar a hipótese em que a resposta ao pedido demande atividades (como a coleta, o agrupamento e a análise de documentos) que não estejam dentre as competências da instituição. Dessa maneira, caso a produção da informação demandada pelo cidadão não seja de competência do órgão requerido - ou este não a tenha sob sua posse -, o Serviço de Informações ao Cidadão deste órgão não está obrigado legalmente a fornecê-la.
                        </p>

                        <p class="Texto">
                            Nas duas hipóteses, acima tratadas, o órgão ou a entidade pública deve indicar o local onde se encontram as informações demandadas, de maneira que o cidadão possa, ele próprio, realizar o trabalho de análise, interpretação e consolidação dos dados.
                        </p>

