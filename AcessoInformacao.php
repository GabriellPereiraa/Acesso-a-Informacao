                            <strong><span class="CorLaranja">I.</span></strong> o respeito à privacidade;
                        </p>

                        <p class="Texto">
                            <strong><span class="CorLaranja">II.</span></strong> a autodeterminação informativa;
                        </p>

                        <p class="Texto">
                            <strong><span class="CorLaranja">III.</span></strong> a liberdade de expressão, de informação, de comunicação e de opinião;
                        </p>

                        <p class="Texto">
                            <strong><span class="CorLaranja">IV.</span></strong> a inviolabilidade da intimidade, da honra e da imagem;
                        </p>

                        <p class="Texto">
                            <strong><span class="CorLaranja">V.</span></strong> o desenvolvimento econômico e tecnológico e a inovação;
                        </p>

                        <p class="Texto">
                            <strong><span class="CorLaranja">VI.</span></strong> a livre iniciativa, a livre concorrência e a defesa do consumidor; e
                        </p>

                        <p class="Texto">
                            <strong><span class="CorLaranja">VII.</span></strong> os direitos humanos e o livre desenvolvimento da personalidade, dignidade e exercício a cidadania pelas pessoas naturais.
                        </p>
