                        <p class="Texto">
                        <h3 id="list-PAcessoInformacao" class="SubSubTitulo">4.1.2 O que não é um pedido de acesso à informação</h3>
                        </p>
                        <p class="Texto">
                            Ao longo dos últimos anos, com a facilidade dos sistemas de informação e o interesse crescente da sociedade nos assuntos da gestão pública, cada vez mais aparecem demandas das mais variadas espécies. Assim, ora estamos diante de um pedido de serviço, ora uma reclamação, ora uma denúncia, ora uma solicitação de informação e, não raro, a combinação, em uma mesma demanda, de mais de uma espécie de pedido. Nesse sentido, se faz necessário o preparo do agente público para a precisa identificação da natureza da demanda o que permitirá a realização das medidas adequadas à satisfação do cidadão.
                        </p>

                        <p class="Texto">
                            Podemos afirmar que um pedido de acesso à informação constitui-se em uma demanda encaminhada ao Poder Público (órgãos e entidades), por qualquer pessoa, física ou jurídica, que <strong>tenha por objeto um dado ou informação</strong>. Essa última (informação) encontra-se conceituada no inciso I do art. 4° da Lei de Acesso à Informação Federal, nos seguintes termos:
                        </p>
                    </div>
                </div>

                <div class="row">
                    <div class="col">

                    </div>

                    <div class="col-9">
                        <p class="Texto">
                            Art. 4º Para os efeitos desta Lei, considera-se:
                            I - informação: dados, processados ou não, que podem ser utilizados para produção e transmissão de conhecimento, contidos em qualquer meio, suporte ou formato;
                            [...]
                        </p>
                    </div>
                </div>

                <div class="row">
                    <div class="col">
                        <p class="Texto">
                            Por vezes, como alertado antes, os pedidos de acesso à informação podem ser <span style="color:red">confundidos</span> com a solicitação de uma<span style="color:red"> prestação de serviço</span>, em outras vezes podem ser formulados conjuntamente com outras demandas tais como, <span style="color:red">reclamações, denúncias, consultas jurídicas.</span> Nesses casos, faz-se necessário a precisa compreensão do objeto de uma solicitação de informação e a separação para o atendimento eficaz do cidadão.
