                        <p class="Texto">
                            <i class="fas fa-compress-arrows-alt CorLaranja"></i><strong> Pedido com objeto inexistente:</strong>
                        </p>
                    </div>
                </div>

                <div class="row">
                    <div class="col">
                    </div>
                    <div class="col-11">
                        <p class="Texto">
                            Ainda que o procedimento da LAI tenha sido desenhado essencialmente para se conceder acesso a uma informação, nem sempre a informação desejada pelo cidadão existe. Tanto é assim que a lei autoriza a instituição pública a “comunicar que não possui a informação”, conforme prescrito no inciso III do §1º de seu art. 11.
                        </p>

                        <p class="Texto">
                            Não obstante o órgão ou entidade não possua a informação solicitada, esses casos não são caracterizados como hipótese de negativa de acesso, devido ao fato de estas informações terem a possibilidade de existir, mesmo que em outro órgão. Dessa forma, como regra geral, a existência do objeto é condição para conhecimento de um pedido de acesso à informação.
                        </p>

                        <p class="Texto">
                            É importante que o órgão/entidade comprove concretamente os esforços realizados na busca pela informação, essencial para o tratamento adequado de um pedido de informação. Ademais, pode o interessado fornecer, no curso do processo administrativo, indícios ou provas de que a informação de fato existe, contrapondo-se à alegação da instituição pública.
