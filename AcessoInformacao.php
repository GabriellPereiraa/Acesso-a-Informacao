                        <h3 id="list-InformacaoPessoal" class="SubSubTitulo">3.2 Informação pública versos informação pessoal</h3>
                        </p>

                        <p class="Texto">
                            Não obstante o conceito de informação pública, acima apresentado, há situações em que podem suscitar dúvidas quanto à natureza pública da informação. Aqui, faremos um contraponto entre a informação pública e a de natureza pessoal.
                        </p>
                        <p class="Texto">
                            A Lei Estadual n° 15.175/12 definiu em seu art. 3°, inciso I, o conceito de Informação de Interesse Público como sendo <span style="color:red">“toda aquela informação que não é de caráter pessoal ou classificada como sigilosa”</span>. Em seguida, no inciso III do mesmo dispositivo, o legislador estadual afirma que Informação Pessoal é <span style="color:blue">“aquela relacionada à pessoa natural identificada ou identificável.”</span> O cerne da questão está na definição do que vem a ser informação de caráter pessoal.
                        </p>
                        <p class="Texto">
                            Tendo em vista que a Lei de Acesso à Informação não definiu de forma clara o que seriam informações referentes à intimidade, à vida privada, à honra e à imagem do indivíduo deve-se buscar outros dispositivos para aclarar o entendimento sobre o assunto. O STJ, na sua resolução que regulamenta a Lei n. 12.527/2011 no Superior Tribunal de Justiça e dá outras providências, exemplifica o que seria informação pessoal, no artigo 3º, inciso V, senão vejamos:
                        </p>
                    </div>
                </div>
                <div class="row">
                    <div class=col>

                    </div>
                    <div class="col-9">
                        V – informação pessoal: aquela que diz respeito à intimidade, à vida privada, à honra e à imagem das pessoas, bem como às liberdades e garantias individuais, tais como endereço, telefones residencial e celular, número de inscrição no cadastro de pessoas físicas (CPF), número da carteira de identidade (RG), carteira funcional e passaporte de magistrados e servidores;
                    </div>
                </div>

                <div class="row">
                    <div class="col">
                        <p class="Texto">
                            Por sua vez a LGPD, <strong> Lei n.º 13.709/2018 </strong>, estabeleceu novas disposições acerca da matéria, a exemplo da definição de dado pessoal sensível (dado pessoal sobre origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico, quando vinculado a uma pessoa natural) dentre outras. A LGPD aplica-se ao tratamento de dados pessoais, inclusive nos meios digitais, por pessoa natural ou pessoa jurídica de <strong>público ou privado</strong>, a partir dos seguintes fundamentos:
