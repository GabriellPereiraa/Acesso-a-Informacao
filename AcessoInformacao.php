                            PROCEDIMENTO ESPECÍFICO – Caso exista canal ou procedimento específico efetivo para obtenção da informação solicitada, o órgão ou a entidade deve orientar o interessado a buscar a informação por intermédio desse canal ou procedimento, indicando os prazos e as condições para sua utilização, sendo o pedido considerado atendido (SÚMULA CRMI, 2015, online).
